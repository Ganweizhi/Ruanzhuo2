package com.dgut.group33.javaBean;

public class HomeWorkCase {
    private int homework_case_id;
    private String course_name;
    private String homework_content;

    public int getHomework_case_id() {
        return homework_case_id;
    }

    public void setHomework_case_id(int homework_case_id) {
        this.homework_case_id = homework_case_id;
    }

    public String getCourse_name() {
        return course_name;
    }

    public void setCourse_name(String course_name) {
        this.course_name = course_name;
    }

    public String getHomework_content() {
        return homework_content;
    }

    public void setHomework_content(String homework_content) {
        this.homework_content = homework_content;
    }
}
